using namespace std;

class debug_cout
{
public:
    debug_cout(bool debug) : debug(debug) {}

    template <typename T>
    debug_cout &operator<<(const T &value)
    {
        if (debug)
        {
            cout << value;
        }
        return *this;
    }

    debug_cout &operator<<(std::ostream &(*manip)(std::ostream &))
    {
        if (debug)
        {
            manip(std::cout);
        }
        return *this;
    }

private:
    bool debug;
};

debug_cout dbgcout(true);

template <typename T>
void output_vector(vector<T> v, bool debug)
{
    for (int i = 0; i < v.size(); i++)
    {
        if (debug)
        {
            dbgcout << v[i] << " ";
        }
        else
        {
            cout << v[i] << " ";
        }
    }
    if (debug)
    {
        dbgcout << endl;
    }
    else
    {
        cout << endl;
    }
}

int main()
{
    int n;
    cin >> n;
    vector<long long> vec(200010, 0);

    for (int i = 0; i < n; ++i)
    {
        long long a;
        cin >> a;
        vec[a]++;
    }

    long long ans = 0;
    for (int i = 0; i < 200010; ++i)
    {
        if (vec[i] >= 2)
        {
            long long a = vec[i];
            long long to_add = (n - a) * a * (a - 1) / 2ll;
            ans += to_add;
        }
    }

    cout << ans << endl;

    return 0;
}
